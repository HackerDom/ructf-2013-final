package ructf.roundsCache;

public class TeamDefenseAttack {
	
	private int team;
	private int defense;
	private int attack;
	
	public TeamDefenseAttack(int team, int defense, int attack) {
		this.team = team;
		this.defense = defense;
		this.attack = attack;
	}
	
	public int getTeam() {
		return team;
	}
	
	public int getDefense() {
		return defense;
	}
	
	public int getAttack() {
		return attack;
	}
}
